# Sample list
my_list = [1, 2, 3, 4, 5]

# Iterating over the list using a for loop
print("Iterating over a list:")
for item in my_list:
    print(item)

# Sample dictionary
my_dict = {'name': 'Alice', 'age': 30, 'city': 'New York'}

# Iterating over the dictionary using a for loop to print keys and values
print("\nIterating over a dictionary:")
for key, value in my_dict.items():
    print(key, ":", value)
